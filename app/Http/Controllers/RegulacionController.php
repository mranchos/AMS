    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $regulacion = new Regulacion();

        $regulacion->name = request('name');
        $regulacion->emisor = request('emisor');
        $regulacion->date = request('date');
        $regulacion->description = request('description');

        $regulacion->save();

        return redirect('/macro');
    }

